    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        
         try{
              ResultSet rs2;
            PreparedStatement ps2;
            Connection con;
             
             
            con=DbConn.dbConnection();
            int row=Table.getSelectedRow();
            String table_click=Table.getModel().getValueAt(row, 0).toString();
              
            String sql= "select user_name,password,user_type from login where user_name like '" + table_click + "'";
            ps2=con.prepareStatement(sql);
            rs2=  ps2.executeQuery();
            if(rs2.next()){
                String add1=rs2.getString("user_name");
                 UserN.setText(add1);
                String add2=rs2.getString("password");
                  password.setText(add2);
                String add3=rs2.getString("user_type");
                 userType.setSelectedItem(add3);
                
               
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,ex);
        }
        
    }//GEN-LAST:event_TableMouseClicked

