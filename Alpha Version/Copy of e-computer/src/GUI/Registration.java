    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
     
         if (UserN.getText().length()==0) {
                JOptionPane.showMessageDialog( this, "Please Input User Name","Error", JOptionPane.ERROR_MESSAGE);
               UserN.requestFocus();
                return;

            }
         if (password.getText().length()==0) {
                JOptionPane.showMessageDialog( this, "Please Input  Password","Error", JOptionPane.ERROR_MESSAGE);
                password.requestFocus();
                return;

            }
         
         
        String sql="insert into login(user_name,password,user_type)values(?,?,?)";
        try {
            ps=con.prepareStatement(sql);
            
              ps.setString(1, UserN.getText());
            
            ps.setString(2, password.getText());
           ps.setString(3, userType.getSelectedItem().toString());
            ps.executeUpdate();
             JOptionPane.showMessageDialog(null, "Data Inserted Successfully");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Data not Inserted ");
        }
        
        getData();
    }//GEN-LAST:event_submitActionPerformed
