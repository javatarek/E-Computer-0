    private void cateCodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cateCodeKeyReleased
        // TODO add your handling code here:
        
         int kCode=evt.getKeyCode();
        if(kCode==10){
        String c=cateCode.getText();
         String sql="select * from category where category_code=?";
         try {
            ps=con.prepareStatement(sql);
            ps.setString(1, c);
            rs=ps.executeQuery();
            if(rs.next()){
                 cateN.setText(rs.getString("category_name"));  
            }else{
                JOptionPane.showMessageDialog(null, "Item not found!");
                             
            }
             
        } catch (Exception e) {
             
        }
        }
        
    }//GEN-LAST:event_cateCodeKeyReleased

