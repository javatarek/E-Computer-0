    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
     
         if (cateN.getText().length()==0) {
                JOptionPane.showMessageDialog( this, "Please Input Category Name","Error", JOptionPane.ERROR_MESSAGE);
                cateN.requestFocus();
                return;

            }
         
         if (cateCode.getText().length()==0) {
                JOptionPane.showMessageDialog( this, "Please Input Category code","Error", JOptionPane.ERROR_MESSAGE);
                cateCode.requestFocus();
                return;

            }
        
        String sql="insert into category(category_name,category_code)values(?,?)";
        try {
            ps=con.prepareStatement(sql);
            ps.setString(1, cateN.getText());
            ps.setString(2, cateCode.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Category Add  Successfully");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Category Already Exist");
        }
        
    }//GEN-LAST:event_addBTNActionPerformed

